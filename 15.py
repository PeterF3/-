a=input('请输入一段字符')
b=0
c=0  #符号
d=0  #数字
e=0  #大写字母
f=0  #小写字母
for i in a:
    b=ord(i)
    if 32<=b<=47:  #空格和符号
        c+=1
    elif 48<=b<=57:  #数字
        d+=1
    elif 58<=b<=64:  #符号
        c+=1
    elif 65<=b<=90:  #大写字母
        e+=1
    elif 91<=b<=96:  #符号
        c+=1
    elif 97<=b<=122:  #小写字母
        f+=1
    else:  #余下的符号
        c+=1
print('符号数为：'+str(c)+'数字数为：'+str(d)+'大写字母数为：'+str(e)+'小写字母数为：'+str(d))
        
    